    return {
        "compatibility": compatibility_score(life_path_a, life_path_b),
        "union_number": {
            "number": union,
            "meaning": UNION_MEANINGS.get(union, ""),
        },
        "age": age_dynamics(birthdate_a, birthdate_b),
        "year_sync": personal_year_sync(personal_year_a, personal_year_b),
    }
