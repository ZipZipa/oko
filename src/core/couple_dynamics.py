# Мастер-числа упрощаем до базовых для таблицы (11→2, 22→4, 33→6)
def _simplify_master(n: int) -> int:
    return {11: 2, 22: 4, 33: 6}.get(n, n)


def compatibility_score(life_path_a: int, life_path_b: int) -> dict:
    a = _simplify_master(life_path_a)
    b = _simplify_master(life_path_b)
    key = tuple(sorted([a, b]))
    result = COMPATIBILITY_MATRIX.get(key, {
        "score": 7,
        "type": "нейтральная пара",
        "desc": "Среднее соответствие, всё зависит от работы пары",
    })
    return {
        "life_path_pair": [life_path_a, life_path_b],
        **result,
    }


def union_number(birthdate1: str, birthdate2: str) -> int:
    """Число союза = сумма всех цифр обеих дат, сведённая."""
    digits = [int(d) for d in (birthdate1 + birthdate2) if d.isdigit()]
    return reduce_number(sum(digits))


