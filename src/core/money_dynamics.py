def calculate_money_ceiling_indicator(life_path: int, day: int) -> dict:
    """
    Индикатор «потолка» — оценка естественной зоны комфорта по доходу.
    Не предсказывает абсолютные суммы, описывает паттерн.
    """
    # Сильные числа по деньгам: 8, 1, 4, 22
    high_money_numbers = {8, 22, 1}
    medium_money_numbers = {4, 6, 3, 5}

    if life_path in high_money_numbers:
        ceiling_type = "высокий потенциал, нелинейный путь"
        desc = "Природные данные для крупного дохода есть. Главное — не остановиться на уровне «хватает». Потолок не в способностях, а в готовности рисковать."
    elif life_path in medium_money_numbers:
        ceiling_type = "стабильный средний-выше среднего"
        desc = "Естественная зона — устойчивый доход выше среднего без скачков. Большие деньги возможны, но требуют выхода из зоны комфорта."
    else:  # 2, 7, 9, 11, 33
        ceiling_type = "доход через мастерство, не через объём"
        desc = "Деньги приходят за уникальное качество, не за масштаб. Скорее премиум-эксперт, чем массовый игрок. Потолок снимается через личный бренд."
    return {"type": ceiling_type, "desc": desc}


