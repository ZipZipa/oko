def calculate_pinnacle_at_age(birthdate: str, age: int) -> int:
    """Какой пинакл активен в данном возрасте."""
    from .numerology import calculate_pinnacles
    pinnacles = calculate_pinnacles(birthdate)
    for p in pinnacles:
        if p["age_end"] is None or age < p["age_end"]:
            if age >= p["age_start"]:
                return p["number"]
    return pinnacles[-1]["number"]


def calculate_money_forecast(birthdate: str, current_year: int = None,
                             years_ahead: int = 5) -> list[dict]:
    """Прогноз на ближайшие N лет."""
    if current_year is None:
        current_year = datetime.now().year

    forecast = []
    for offset in range(years_ahead + 1):
        year = current_year + offset
        py = personal_year(birthdate, year)
        forecast.append({
            "year": year,
            "personal_year": py,
            "tone": _money_tone_for_personal_year(py),
        })
    return forecast


