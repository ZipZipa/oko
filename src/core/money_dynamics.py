# Денежный код = число дня + месяц + последняя цифра года (упрощённый «вибрационный код»)
def calculate_money_code(birthdate: str) -> dict:
    day, month, year = parse_birthdate(birthdate)
    last_year_digit = year % 10

    raw = day + month + last_year_digit
    code = reduce_number(raw)

    return {
        "number": code,
        "raw": raw,
        "formula": f"{day} + {month} + {last_year_digit} = {raw} → {code}",
    }


